package MenuDriven;

public class Rectangle {
	// prints area of rectangle
	void areaOfRect(float length, float breadth) {
		System.out.print("Area of circle:" + (length * breadth));
	}

	// prints perimeter of rectangle
	void PeriOfRec(float length, float breadth) {
		System.out.print("Circumference of circle:" + ((length + breadth) * 2));
	}
}
