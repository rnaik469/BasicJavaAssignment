package MenuDriven;

public class Circle {
	// prints areaCircle
	void areaCircle(float radius) {
		System.out.print("Area of circle:" + (3.14f * radius * radius));
	}

	// prints circumfOfCircle
	void circumfOfCircle(float radius) {
		System.out.print("Circumference of circle:" + (3.14f * 2 * radius));
	}
}
